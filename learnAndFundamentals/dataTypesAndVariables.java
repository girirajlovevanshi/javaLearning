public class dataTypesAndVariables {
    public static void main(String args[]) {
        // code
        System.out.println("Data Types and Variables");

        // Case Sensitivity - Java is case sensitive, which means identifier Hello
        // and hello would have different meaning in Java.

        // Data Types
        // Java is a statically-typed language. This means that all variables must be
        // declared before they can be used.
        // int, float, char, boolean, double, byte, short, long
        // int - 32 bit
        // float - 32 bit
        // char - 16 bit
        // boolean - 1 bit
        // double - 64 bit
        // byte - 8 bit
        // short - 16 bit
        // long - 64 bit

        // Variables
        // A variable is a container which holds the value while the Java program is
        // executed.
        // A variable is assigned with a data type.
        // Variable is a name of memory location.
        // There are three types of variables in Java: local, instance and static.
        
        
        // Local Variables
        // A variable declared inside the body of the method is called local variable.
        // You can use this variable only within that method and the other methods in
        // the class aren't even aware that the variable exists.
        // A local variable cannot be defined with "static" keyword.

        // Instance Variables
        // A variable declared inside the class but outside the body of the method, is
        // called instance variable. It is not declared as static.

        // Static Variables
        // A variable which is declared as static is called static variable. It cannot
        // be local. You can create a single copy of static variable and share among
        // all the instances of the class.



    }

}